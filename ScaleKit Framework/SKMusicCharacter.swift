import UIKit

/**
SKMusicCharacter

- Flat:        Symbol to be used for flats
- DoubleFlat:  Symbol to be used for double flats
- Sharp:       Symbol to be used for sharps
- DoubleSharp: Symbol to be used for double sharps
*/
public enum SKMusicCharacter: String {
    
    case Flat = "b"
    case DoubleFlat = "bb"
    case Sharp = "#"
    case DoubleSharp = "x"
    
}
