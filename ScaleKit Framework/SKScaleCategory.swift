import Foundation

/**
Category which defines related scaleTypes

- Standard
- Blues
- Jazz
- European
- Asiatic
- Modal
*/
public enum SKScaleCategory : String{
    
    case Standard = "Standard"
    case Blues = "Blues"
    case Jazz = "Jazz"
    case European = "European"
    case Asiatic = "Asiatic"
    case Modal = "Modal"
    
}